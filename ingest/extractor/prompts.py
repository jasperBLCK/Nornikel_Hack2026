GROUNDING_SYSTEM_PROMPT = """\
Ты — строгий рецензент. Тебе дан фрагмент исходного текста и JSON-извлечение, \
полученное из этого фрагмента другой моделью. Твоя задача — оценить, НАСКОЛЬКО \
извлечение ОБОСНОВАНО текстом.

Оцени только то, что прямо подтверждается текстом. Любое утверждение, не \
подтверждённое текстом, считай галлюцинацией.

Ответь СТРОГО JSON без пояснений в формате:
{
  "score": <число от 0.0 до 1.0 — доля полей, обоснованных текстом>,
  "rationale": "<краткое пояснение до 30 слов>",
  "action": "accept" если score >= 0.6, иначе "reject"
}
"""


def build_extraction_messages(document_id: str, chunk_id: str, text: str) -> list[dict]:
    """Build a multi-turn message list with few-shot priming.

    Final user message carries the actual chunk.
    """
    return [
        {"role": "user", "content": json.dumps(
            FEW_SHOT_EXAMPLE_1_INPUT, ensure_ascii=False
        )},
        {"role": "assistant", "content": json.dumps(
            FEW_SHOT_EXAMPLE_1_OUTPUT, ensure_ascii=False
        )},
        {
            "role": "user",
            "content": json.dumps(
                {"document_id": document_id, "chunk_id": chunk_id, "text": text},
                ensure_ascii=False,
            ),
        },
    ]


def build_grounding_messages(chunk_text: str, extracted: dict) -> list[dict]:
    return [
        {
            "role": "user",
            "content": json.dumps(
                {"source_text": chunk_text, "extracted": extracted},
                ensure_ascii=False,
            ),
        },
    ]